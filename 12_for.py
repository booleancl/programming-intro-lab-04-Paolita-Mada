# El for es una estructura de control que nos permite realizar la misma accion, pero con elementos diferentes cada vez.

veggies = ['Broccoli', 'Celery', 'Lettucce', 'Carrot']

for veggies in veggies:
    print('I need to eat more',veggies)

fruits = ['Apple','Strawberry','Banana','Orange']

for fruit in fruits:
    print('I love to eat', fruit)
    


